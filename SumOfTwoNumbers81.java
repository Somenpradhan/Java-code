class Sample 
{
    public static void main(String[] args) 
    {
        int a = 5;
        int b = 3;
        int result = sum(a, b);
        System.out.println("The sum of " + a + " and " + b + " is: " + result);
    }

    public static int sum(int num1, int num2) 
    {
        return num1 + num2;
    }
}
